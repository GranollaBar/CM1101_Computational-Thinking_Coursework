def draw_gavel_animation():
    for i in range(3):
        os.system('cls')
        draw_gavel_raised()
        time.sleep(float(random.randint(10, 20)/100))
        winsound.PlaySound(f"{os.getcwd()}/music_sound_effects/gavel.wav", winsound.SND_FILENAME | winsound.SND_ASYNC)
        os.system('cls')
        draw_gavel_down()
        time.sleep(float(random.randint(10, 20)/100))
        os.system('cls')
        draw_gavel_raised()
    print("\n\n")


